import mongoose from "mongoose";

const reviewScheme = new mongoose.Schema(
  {
    user: {
      type: mongoose.Schema.Type.ObjectId,
      required: true,
      ref: "User",
    },
    name: {
      type: String,
      required: true,
    },
    rating: {
      type: Number,
      required: True,
    },
    comment: {
      type: String,
      required: True,
    },
  },
  {
    timestamps: true,
  }
);

const productScheme = new mongoose.Schema(
  {
    user: {
      type: mongoose.Schema.Type.ObjectId,
      required: true,
      ref: "User",
    },
    name: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
    brand: {
      type: String,
      required: true,
    },
    category: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    reviews: [reviewScheme],
    rating: {
      type: Number,
      required: true,
      default: 0,
    },
    numReviews: {
      type: Number,
      required: true,
      default: 0,
    },
    price: {
      type: Number,
      required: true,
      default: 0,
    },
    countInStock: {
      type: Number,
      required: true,
      default: 0,
    },
  },
  {
    timestamps: true,
  }
);

const Product = mongoose.model("Product", productScheme);

export default Product;
